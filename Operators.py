"""
An operators is a symbol that performs a certain orperation between operands
 1] Arithmetic Operators ( +,-,*,/,%,**)
 2] Relational / Comparison Operators (==,!=,>,<,>=,<=)
 3] Assignment Operators(=,+=,-=,*=,/=,%=,**=)
 4] Logical Operators (not,and,or)

"""


# 1] Arithmetic Operators

a=9
b=10
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)


# 2] Relational / Comparison Operators (==,!=,>,<,>=,<=)

a=50
b=20

print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)

